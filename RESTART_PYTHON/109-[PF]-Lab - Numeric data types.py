#Creating a Variable
myValue=1
print(myValue)

#To get the data type of the variable, use the type() built-in function:
print(type(myValue))

#To combine numbers and text, use the str() built-in function, which converts an 
# argument into a collection of letters called a string. In this instance,
# you are converting the int (integer) data type into the string data type:
print(str(myValue) + " is of the data type" + str(type(myValue)))

